// Create a new heading element
const heading = document.createElement("h1");

// Set the text content of the heading to "amongus"
heading.textContent = "amongus";

// Set the font size of the heading to 30
heading.style.fontSize = "30px";

// Set the text alignment of the heading to center
heading.style.textAlign = "center";

// Add the heading element to the body of the page
document.body.appendChild(heading);

